/**
 * A utility class which houses the color codes for all colors we are using.
 * @author heshamsalman
 *
 */
public class Colors {
	public static Color unselected() {
		return new Color(0xD7D7D7);
	}
	
	public static Color selected() {
		return new Color(0x000000);
	}
}
